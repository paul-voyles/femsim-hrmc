  !*************************************************
  !*************Three scattering functions**********
  !*************************************************
  
  FUNCTION f_e(Z,k)
    
    IMPLICIT NONE
    REAL f_e
    INTEGER Z
    REAL k
    !REAL, DIMENSION(103,3) :: a_e, b_e, c_e, d_e
    
    
    !In electron atomic scattering factor calculation
    !the following equation is used based on Kirkland book
    !f_e(k)=Sigma(a_i/(k^2+b_i)) + Sigma(c_i*exp(-d_i*k^2))
    !Three sets of a,b,c and d are provided
    
    INTEGER i
    
    f_e=0.0
    
    DO i=1, 3
       f_e=f_e + a_e(Z,i)/(k**2+b_e(Z,i))+c_e(Z,i)*exp(-d_e(Z,i)*k**2)
    ENDDO
    
    
    
  END FUNCTION f_e

