!*******function  f_x(Z,k,a_x, b_x, c_x) calculate X-ray scattering factor
!*******for element Z at scattering vector k*********
!*******************************************************
FUNCTION f_x(Z,k)

IMPLICIT NONE

 REAL f_x
 INTEGER Z
 REAL k
 !REAL, DIMENSION(98,4) :: a_x, b_x
 !REAL, DIMENSION(98) :: c_x

 INTEGER i

 f_x=0.0

 DO i=1, 4
   f_x = f_x + a_x(Z,i)*exp(-b_x(Z,i)*k**2)
 ENDDO

 f_x = f_x + c_x(Z)




END FUNCTION f_x


